    private void cargarDatosMongoDB() {
        MongoClient client = ConexionMongo.getMongoClient();
    if (client == null) {
        JOptionPane.showMessageDialog(this, "No se pudo conectar a MongoDB", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    MongoDatabase database = client.getDatabase("bd_restaurante");
    MongoCollection<Document> collection = database.getCollection("ordenes");

    // Recuperar todas las Ã³rdenes
    FindIterable<Document> documents = collection.find();

    // Crear un mapa para agrupar y contar los platillos
    HashMap<String, Integer> platillosMap = new HashMap<>();

    for (Document doc : documents) {
        // Obtener el arreglo de platillos
        List<Document> platillos = (List<Document>) doc.get("platillos");

        // Procesar cada platillo en el arreglo
        for (Document platillo : platillos) {
            String nombrePlatillo = platillo.getString("nombre");
            int cantidad = platillo.getInteger("cantidad", 1); // Por defecto, 1 si no hay cantidad

            // Sumar al mapa
            platillosMap.put(nombrePlatillo, platillosMap.getOrDefault(nombrePlatillo, 0) + cantidad);
        }
    }

    // Construir el texto para el JTextArea
    StringBuilder sb = new StringBuilder();
    sb.append("-----------Platillos a preparar--------------:\n");
    for (Map.Entry<String, Integer> entry : platillosMap.entrySet()) {
        sb.append(entry.getKey()).append(": ").append(entry.getValue()).append("\n");
    }

    // Mostrar el resultado en el JTextArea
    jTextArea1.setText(sb.toString());
    }

